// Генераторы аналогов ОГЭ по биологии (предмет «ОГЭ Биология»).
// Эталон структуры/типажей — fipi_bank_bio/ (открытый банк ФИПИ). Контент СВОЙ.
// В выгрузке ФИПИ ключей нет → КАЖДЫЙ генератор строит задание так, что правильный
// ответ известен ПО ПОСТРОЕНИЮ (истинность суждений/принадлежность/порядок заданы нами
// и выверены вручную). Форма возврата — { condition_text, answer } (как у обществознания).
//
// Первый батч (этап 2): текстовые номера с однозначным ключом —
//   №02 соответствие (организм → царство), №05 последовательность (усложнение/системы),
//   №09 множественный выбор (приспособленность/признаки), №12 суждения А-Б,
//   №15 одиночный выбор (организм человека).
// Развёрнутые №22–26 (сочинение/эксперимент) и картиночные номера — отдельный этап.
//
// Экспортируем GENERATORS_BIO (номер → [функции]) и GEN_META_BIO (номер → [[тема,[[ключ,подпись,fn]]]]);
// их подхватывает диспетчер в taskGenerators.js под ключом "ОГЭ Биология".

import { matchBlock, listBlock } from "../utils"

const randInt = (a, b) => a + Math.floor(Math.random() * (b - a + 1))
const pick = (a) => a[Math.floor(Math.random() * a.length)]
const shuffle = (a) => { a = a.slice(); for (let i = a.length - 1; i > 0; i--) { const j = randInt(0, i);[a[i], a[j]] = [a[j], a[i]] } return a }
const digitsAsc = (arr) => arr.slice().sort((x, y) => x - y).join("")

// ── Движок «Верны ли суждения А и Б» (№12) ────────────────────────────────────
// pool: { <тема>: [{ t, v:true|false }, …] }. Оба суждения — об одной теме.
// Ответ: ТЛ→1, ЛТ→2, ТТ→3, ЛЛ→4.
function makeAB(pool) {
  const topics = Object.keys(pool)
  return () => {
    const topic = pick(topics)
    const [a, b] = shuffle(pool[topic]).slice(0, 2)
    const code = a.v && b.v ? 3 : a.v && !b.v ? 1 : !a.v && b.v ? 2 : 4
    const ct =
      `Выберите правильный ответ.\n` +
      `Верны ли следующие суждения о ${topic}?\n` +
      `А. ${a.t}\n` +
      `Б. ${b.t}\n` +
      `1)  верно только А\n` +
      `2)  верно только Б\n` +
      `3)  верны оба суждения\n` +
      `4)  оба суждения неверны`
    return { condition_text: ct, answer: String(code) }
  }
}

// ── Движок «Одиночный выбор из 4» (№15) ───────────────────────────────────────
// pool: [{ q, correct, wrong:[…≥3] }].
function makeChoice(pool) {
  return () => {
    const it = pick(pool)
    const wrong = shuffle(it.wrong).slice(0, 3)
    const opts = shuffle([{ t: it.correct, ok: true }, ...wrong.map((t) => ({ t, ok: false }))])
    const ans = opts.findIndex((o) => o.ok) + 1
    const lines = opts.map((o, i) => `${i + 1})  ${o.t}`).join("\n")
    return { condition_text: `Выберите правильный ответ.\n${it.q}\n${lines}`, answer: String(ans) }
  }
}

// ── Движок «Выберите три верных ответа из шести» (№09) ─────────────────────────
// pool: [{ q, correct:[3], wrong:[≥3] }]. Ответ — три цифры по возрастанию.
function makeMulti3(pool) {
  return () => {
    const it = pick(pool)
    const wrong = shuffle(it.wrong).slice(0, 3)
    const opts = shuffle([...it.correct.map((t) => ({ t, ok: true })), ...wrong.map((t) => ({ t, ok: false }))])
    const ans = digitsAsc(opts.map((o, i) => (o.ok ? i + 1 : 0)).filter(Boolean))
    const lines = opts.map((o, i) => `${i + 1})   ${o.t}`).join("\n")
    return {
      condition_text:
        `Выберите один или несколько правильных ответов.\n${it.q} ` +
        `Выберите три верных ответа из шести и запишите в таблицу цифры, под которыми они указаны.\n${lines}`,
      answer: ans,
    }
  }
}

// ── Движок «Установите последовательность» (№05, №03, №20) ────────────────────
// order — элементы в ПРАВИЛЬНОМ порядке; показываем перемешанными, ответ — номера
// показанных пунктов в правильном порядке. tail — концовка формулировки (у №03 иная).
const SEQ_TAIL = "В ответе запишите соответствующую последовательность цифр."
function renderSeq(q, order, tail = SEQ_TAIL) {
  const disp = shuffle(order)                            // как показываем (1..n)
  const ans = order.map((t) => disp.indexOf(t) + 1).join("")
  return { condition_text: `Установите последовательность.\n${q} ${tail}\n${listBlock(disp)}`, answer: ans }
}
// pool: [{ q, order }].
function makeSeq(pool, tail = SEQ_TAIL) {
  return () => { const it = pick(pool); return renderSeq(it.q, it.order, tail) }
}

// ── Движок «Установите соответствие» (№02) ────────────────────────────────────
// cats — полный список категорий (2-й столбец); items — [{ name, cat }] (cat ∈ cats).
// Берём 4 объекта (А-Г), номера категорий перемешиваем. Ответ — 4 цифры для А,Б,В,Г.
function makeMatch(title, leftHead, rightHead, cats, items) {
  return () => {
    const four = shuffle(items).slice(0, 4)
    const catOrder = shuffle(cats)                       // как нумеруем категории
    const catNum = (c) => catOrder.indexOf(c) + 1
    const ans = four.map((it) => catNum(it.cat)).join("")
    return {
      condition_text:
        `Установите соответствие и впишите ответ.\n${title}\n\n` +
        matchBlock({ leftHdr: leftHead, rightHdr: rightHead, left: four.map((it) => it.name), right: catOrder }),
      answer: ans,
    }
  }
}

// ── Движок «Соответствие на ДВЕ категории» (№08, №11, №18) ────────────────────
// catNames — [имя0, имя1]; items — [{ t, cat:0|1 }]. Берём count признаков (А…),
// номера двух категорий перемешиваем. Гарантируем, что обе категории представлены.
function makeMatch2(intro, leftHead, rightHead, catNames, items, count = 6) {
  return () => {
    let chosen
    do { chosen = shuffle(items).slice(0, count) }
    while (!(chosen.some((c) => c.cat === 0) && chosen.some((c) => c.cat === 1)))
    const order = shuffle([0, 1])                        // как нумеруем категории
    const catNum = (c) => order.indexOf(c) + 1
    const ans = chosen.map((it) => catNum(it.cat)).join("")
    return {
      condition_text:
        `Установите соответствие и впишите ответ.\n${intro}\n\n` +
        matchBlock({ leftHdr: leftHead, rightHdr: rightHead, left: chosen.map((it) => it.t), right: order.map((c) => catNames[c]) }),
      answer: ans,
    }
  }
}

// ── Движок «Соответствие на N категорий, M объектов» (№21) ────────────────────
// catNames — все категории (2-й столбец, нумерацию перемешиваем); items — [{ t, cat }]
// (cat ∈ catNames); count — сколько объектов А… показать. Гарантируем ≥2 разных категорий.
function makeMatchN(intro, leftHead, rightHead, catNames, items, count = 5) {
  return () => {
    let chosen
    do { chosen = shuffle(items).slice(0, count) }
    while (new Set(chosen.map((c) => c.cat)).size < 2)
    const order = shuffle(catNames)
    const catNum = (c) => order.indexOf(c) + 1
    const ans = chosen.map((it) => catNum(it.cat)).join("")
    return {
      condition_text:
        `Установите соответствие и впишите ответ.\n${intro}\n\n` +
        matchBlock({ leftHdr: leftHead, rightHdr: rightHead, left: chosen.map((it) => it.t), right: order }),
      answer: ans,
    }
  }
}

// ── Движок «Вставьте пропущенные элементы в текст» (№10) ──────────────────────
// text содержит «__________ (А)»…«(Г)»; blanks — верные элементы для А…Г (в порядке текста);
// distractors — лишние. Список из 8 элементов нумеруем случайно; ответ — цифры по тексту.
function makeTerms(pool) {
  return () => {
    const it = pick(pool)
    const list = shuffle([...it.blanks, ...it.distractors])
    const ans = it.blanks.map((el) => list.indexOf(el) + 1).join("")
    return {
      condition_text:
        `Установите соответствие и впишите ответ.\n` +
        `Вставьте в текст «${it.title}» пропущенные элементы из предложенного перечня, используя ` +
        `для этого цифровые обозначения. Запишите в текст цифры выбранных ответов, а затем ` +
        `получившуюся последовательность цифр (по тексту) впишите в приведённую ниже таблицу.\n` +
        `${it.title.toUpperCase()}\n${it.text}\nСписок элементов:\n${listBlock(list)}`,
      answer: ans,
    }
  }
}

