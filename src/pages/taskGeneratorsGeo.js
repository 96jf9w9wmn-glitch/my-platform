// Генераторы аналогов ОГЭ по географии (предмет «ОГЭ География»).
// Эталон структуры/типажей/формулировок — fipi_bank_geo/ (открытый банк ФИПИ). Контент СВОЙ.
// В выгрузке ФИПИ ключей нет → КАЖДЫЙ генератор строит задание так, что правильный ответ
// известен ПО ПОСТРОЕНИЮ (вычисляется формулой или следует из фиксированного закона —
// закона часовых зон РФ). Форма возврата — { condition_text, answer }, как в остальных предметах.
//
// ПЕРВЫЙ ЭТАП — только НОМЕРА БЕЗ КАРТИНОК, где ответ однозначно проверяем вычислением:
//   №06 — относительная влажность воздуха (RH = содержание/максимум · 100 %);
//   №08 — масса солей по солёности (m = S‰ · V, S дана в условии);
//   №13 — температура на высоте по лапс-рейту 0,6 °C/100 м;
//   №26 — порядок встречи Нового года (по часовым зонам РФ, закон о времени).
// Остальные текстовые номера (01–04, 07, 14–17, 20–25, 27–30) — на следующий этап: им нужна
// выверенная фактологическая база (координаты городов, факты по геосферам, статистика и т.п.).
// Картиночные №05, 09–12, 18 — отдельно (нужны карты/планы/профили/климатограммы).
//
// Экспортируем GENERATORS_GEO (номер → [функции]) и GEN_META_GEO (номер → [[тема,[[ключ,подпись,fn]]]]);
// их подхватывает диспетчер в taskGenerators.js под ключом «ОГЭ География».

const randInt = (a, b) => a + Math.floor(Math.random() * (b - a + 1))
const pick = (a) => a[Math.floor(Math.random() * a.length)]
const shuffle = (a) => { a = a.slice(); for (let i = a.length - 1; i > 0; i--) { const j = randInt(0, i);[a[i], a[j]] = [a[j], a[i]] } return a }
const sampleN = (a, n) => shuffle(a).slice(0, n)

// Русское число с запятой (одна десятичная): 3.4 → «3,4», 12 → «12».
const rus1 = (x) => (Math.round(x * 10) / 10).toString().replace(".", ",")
// Температура со знаком, как в ФИПИ: 5 → «+5», −5 → «–5», 0 → «0».
const signT = (x) => (x > 0 ? "+" + x : x < 0 ? "–" + Math.abs(x) : "0")

// ═══════════════ №06 · Относительная влажность воздуха ═══════════════
// RH = (фактическое содержание пара / максимально возможное) · 100 %, округление до целого.
// Максимум берём из стандартной таблицы насыщения (г/м³); опорные пары (−5→3,4; +5→6,8;
// +15→12,8) совпадают с банком ФИПИ. Фактическое содержание — доля максимума → RH реалистична.
const SATUR = [
  { t: -15, max: 1.6 }, { t: -10, max: 2.4 }, { t: -5, max: 3.4 }, { t: 0, max: 4.8 },
  { t: 5, max: 6.8 }, { t: 10, max: 9.4 }, { t: 15, max: 12.8 }, { t: 20, max: 17.3 }, { t: 25, max: 23.0 },
]
function t06Humidity() {
  const s = pick(SATUR)
  // фактическое содержание — от 35 % до 95 % максимума, округлённое до 0,1 г/м³
  const actual = Math.round(s.max * randInt(35, 95) / 100 * 10) / 10
  const rh = Math.round(actual / s.max * 100)
  const condition_text =
    "Впишите правильный ответ.\n" +
    `Температура воздуха равна ${signT(s.t)} °С, содержание водяного пара в нём ${rus1(actual)} г/м³. ` +
    "Какова относительная влажность воздуха, если максимально возможное содержание водяного пара " +
    `при такой температуре составляет ${rus1(s.max)} г/м³? Полученный результат округлите до целого числа.`
  return { condition_text, answer: `${rh}` }
}

// ═══════════════ №08 · Масса солей по солёности ═══════════════
// Солёность S‰ = граммов солей на 1 кг (≈1 л) воды → в V л растворено m = S · V граммов.
// S задаём в условии (реальная средняя солёность моря) → ответ верен независимо от таблицы.
const SEAS = [
  { name: "Красного моря", s: 42 }, { name: "Средиземного моря", s: 38 },
  { name: "Аравийского моря", s: 36 }, { name: "Японского моря", s: 34 },
  { name: "Чёрного моря", s: 18 }, { name: "Каспийского моря", s: 13 },
  { name: "Азовского моря", s: 11 }, { name: "Балтийского моря", s: 7 },
]
function t08Salinity() {
  const sea = pick(SEAS)
  const v = randInt(2, 6)
  const condition_text =
    "Впишите правильный ответ.\n" +
    `Средняя солёность поверхностных вод ${sea.name} составляет ${sea.s}‰. ` +
    `Определите, сколько граммов солей растворено в ${v} л его воды. Ответ запишите в виде числа.`
  return { condition_text, answer: `${sea.s * v}` }
}

// ═══════════════ №13 · Температура воздуха на высоте ═══════════════
// В тропосфере температура понижается на 0,6 °С на каждые 100 м → T_верш = T_подн − 0,6·(H/100).
// Высоту берём кратной 500 м (H/100 кратно 5) → понижение и ответ — целые.
function t13Altitude() {
  const height = randInt(2, 16) * 500          // 1000…8000 м
  const baseT = randInt(2, 6) * 5              // 10…30 °С
  const top = baseT - 0.6 * (height / 100)     // всегда целое
  const variant = pick([0, 1])
  const tail = "Ответ запишите в виде числа."
  const condition_text = variant === 0
    ? "Впишите правильный ответ.\n" +
      `Определите температуру воздуха на вершине горы А с абсолютной высотой ${height} м, ` +
      `если у её подножия, расположенного на уровне моря, значение температуры воздуха составляет ${baseT} °С ` +
      `и известно, что температура понижается на 0,6 °С на каждые 100 м. ${tail}`
    : "Впишите правильный ответ.\n" +
      "При подъёме в тропосфере температура воздуха понижается в среднем на 0,6 °С на каждые 100 м. " +
      `Определите температуру на вершине горы А с абсолютной высотой ${height} м, ` +
      `если у её подножия, расположенного на уровне моря, температура воздуха составляет ${baseT} °С. ${tail}`
  return { condition_text, answer: signT(top) }
}

// ═══════════════ №26 · Порядок встречи Нового года (часовые зоны РФ) ═══════════════
// Восточнее — раньше встречают Новый год: порядок = по убыванию смещения от МСК.
// Смещения фиксированы законом «Об исчислении времени»; берём однозонные регионы.
const TZ = [
  { r: "Калининградская область", off: -1 }, { r: "Москва", off: 0 },
  { r: "Самарская область", off: 1 }, { r: "Свердловская область", off: 2 },
  { r: "Омская область", off: 3 }, { r: "Красноярский край", off: 4 },
  { r: "Иркутская область", off: 5 }, { r: "Забайкальский край", off: 6 },
  { r: "Приморский край", off: 7 }, { r: "Магаданская область", off: 8 },
  { r: "Камчатский край", off: 9 },
]
function t26NewYear() {
  const three = sampleN(TZ, 3)                     // случайные 3 региона, разные смещения
  const listed = shuffle(three)                    // порядок предъявления (нумерация 1–3)
  const order = [...listed].sort((a, b) => b.off - a.off)  // раньше встречают — восточнее
  const answer = order.map((x) => listed.indexOf(x) + 1).join("")
  const condition_text =
    "Установите последовательность.\n" +
    "Расположите регионы России в той последовательности, в которой их жители встречают Новый год, — " +
    "от того, кто встречает его раньше, к тому, кто встречает позже. " +
    "Запишите в ответ получившуюся последовательность цифр.\n" +
    listed.map((x, i) => `${i + 1}) ${x.r}`).join("\n")
  return { condition_text, answer }
}

