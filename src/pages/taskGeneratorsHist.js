  const options = ["увеличилась", "уменьшилась", maxCountry, notMax, "нельзя определить по данной таблице"]
  const aAns = up ? 1 : 2
  const rows = countries.map((c, i) => `${c}\t${vals[i][0]}\t${vals[i][1]}\t${vals[i][2]}`).join("\n")
