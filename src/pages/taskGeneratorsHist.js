// ═══════════════ №04 · Множественный выбор (две позиции) ═══════════════
const ROMAN = { 8: "VIII", 9: "IX", 10: "X", 11: "XI", 12: "XII", 13: "XIII", 14: "XIV", 15: "XV", 16: "XVI", 17: "XVII", 18: "XVIII", 19: "XIX", 20: "XX" }
const centuryOf = (y) => Math.floor((y - 1) / 100) + 1

// Сборка задания «выберите две»: 2 верных + 3 неверных, ответ — две цифры по возрастанию.
function build04(question, right2, wrong3) {
  const items = shuffle([...right2.map((t) => ({ t, ok: true })), ...wrong3.map((t) => ({ t, ok: false }))])
  const ans = items.map((o, i) => (o.ok ? i + 1 : null)).filter(Boolean).sort((a, b) => a - b).join("")
  const list = items.map((o, i) => `${i + 1})   ${o.t}`).join("\n")
  return { condition_text: "Выберите один или несколько правильных ответов.\n" + question + "\n" + list, answer: ans }
}

// «Какие события относятся к <век> в.?» — век определяется по году из EVENTS (верно по построению).
function t04Century() {
  const byCent = {}
  for (const ev of EVENTS) (byCent[centuryOf(ev.y)] ||= []).push(ev)
  const cents = Object.keys(byCent).filter((c) => byCent[c].length >= 2).map(Number)
  const c = pick(cents)
  const right2 = sampleN(byCent[c], 2).map((x) => x.e)
  const wrong3 = sampleN(EVENTS.filter((x) => centuryOf(x.y) !== c), 3).map((x) => x.e)
  const q = `Какие из перечисленных событий относятся к ${ROMAN[c]} в.? Найдите в приведённом списке два события и запишите цифры, под которыми они указаны.`
  return build04(q, right2, wrong3)
}

