// Типаж B: «Вася и Петя» — неоднозначный символьный код, но буквы в сообщении не повторяются.
function t2NoRepeat() {
  for (let tries = 0; tries < 300; tries++) {
    const symbols = pick([["~", "*", "@"], ["*", "@", "+"]])
    const word = distinctWord()
    const letters = [...new Set(word.split(""))]
    const decoys = shuffle(RUS_LETTERS.filter((l) => !letters.includes(l))).slice(0, randInt(1, 2))
    const all = shuffle([...letters, ...decoys])
    const codes = ambiguousCode(all, symbols, [1, 2, 2, 3])
    if (!codes) continue
    const codeMap = Object.fromEntries(all.map((L, i) => [L, codes[i]]))
    const message = word.split("").map((l) => codeMap[l]).join("")
    // условие «без повторов» должно быть НЕОБХОДИМЫМ: без него ≥2 расшифровок, с ним — ровно 1 (= слово)
    if (decodings2(codeMap, message, 2).length < 2) continue
    const nr = decodingsNoRepeat(codeMap, message, 2)
    if (nr.length !== 1 || nr[0] !== word) continue
    return {
      answer: word,
      condition_text:
        `Вася и Петя играли в шпионов и кодировали сообщения собственным шифром. Фрагмент кодовой ` +
        `таблицы приведён ниже.`,
      image_url: codeTableSvg(all, codes),
      condition_tail:
        `Расшифруйте сообщение, если известно, что буквы в нём не повторяются:\n` +
        `${spaced2(message)}\nЗапишите в ответе расшифрованное сообщение.`,
    }
  }
  return t2NoRepeat()
}

// Типаж C: «Валя» — среди трёх цепочек найти ту, что расшифровывается единственным способом.
function t2UniqueChain() {
  for (let tries = 0; tries < 200; tries++) {
    const letters = shuffle(RUS_LETTERS).slice(0, 6)
    const codes = ambiguousCode(letters, ["0", "1"], [2, 2, 3, 3])
    if (!codes) continue
    const codeMap = Object.fromEntries(letters.map((L, i) => [L, codes[i]]))
    const uniq = [], ambig = []
    let example = null
    for (let g = 0; g < 500; g++) {
      const chain = Array.from({ length: randInt(3, 6) }, () => pick(letters)).map((l) => codeMap[l]).join("")
      if (chain.length < 6 || chain.length > 12) continue
      const dec = decodings2(codeMap, chain, 3)
      if (dec.length === 1) { if (!uniq.includes(chain)) uniq.push(chain) }
      else if (dec.length >= 2) { if (!ambig.includes(chain)) ambig.push(chain); if (!example) example = [chain, dec[0], dec[1]] }
    }
    if (!uniq.length || ambig.length < 2 || !example) continue
    const uChain = pick(uniq)
    const distract = shuffle(ambig.filter((c) => c !== uChain)).slice(0, 2)
    if (distract.length < 2) continue
    const three = shuffle([uChain, ...distract])
    return {
      answer: decodings2(codeMap, uChain, 2)[0],
      condition_text: `Валя шифрует русские слова, записывая вместо каждой буквы её код (коды букв даны в таблице).`,
      image_url: codeTableSvg(letters, codes),
      condition_tail:
        `Некоторые кодовые цепочки можно расшифровать не одним способом. Например, ${example[0]} ` +
        `может означать не только ${example[1]}, но и ${example[2]}. Даны три кодовые цепочки: ` +
        `${three.join(", ")}. Найдите среди них ту, которая имеет только одну расшифровку, и запишите ` +
        `в ответе расшифрованную последовательность букв.`,
    }
  }
  return t2UniqueChain()
}

// ── №04 «Кратчайший путь» (КЭС 2.11), подтип с ТАБЛИЦЕЙ расстояний ─────────────
// Симметричная матрица длин дорог (пустая клетка = нет дороги). Найти длину кратчайшего
// пути X→Y (иногда «через пункт C»), при условии «каждый пункт посещается один раз».
// Перебираем ВСЕ простые пути (≤6 узлов) → минимум. Матрицу рисуем настоящей таблицей (SVG).
function matrixTableSvg(nodes, W) {
  const N = nodes.length, S = 46, F = 17
  const rectAt = (cx, cy, fill) => `<rect x="${cx * S}" y="${cy * S}" width="${S}" height="${S}" fill="${fill}" stroke="#333"/>`
  const textAt = (cx, cy, txt, bold) => txt === "" ? "" : `<text x="${cx * S + S / 2}" y="${cy * S + S / 2 + 6}" text-anchor="middle" font-size="${F}" font-family="Arial, sans-serif" fill="#111"${bold ? ' font-weight="bold"' : ""}>${escXml(txt)}</text>`
  let el = rectAt(0, 0, "#dcdcdc")
  for (let i = 0; i < N; i++) el += rectAt(i + 1, 0, "#dcdcdc") + textAt(i + 1, 0, nodes[i], true) + rectAt(0, i + 1, "#dcdcdc") + textAt(0, i + 1, nodes[i], true)
  for (let r = 0; r < N; r++) for (let c = 0; c < N; c++) el += rectAt(c + 1, r + 1, r === c ? "#c9c9c9" : "#fff") + textAt(c + 1, r + 1, W[r][c] > 0 ? String(W[r][c]) : "", false)
  const P = (N + 1) * S
  return svgUrl(`<svg xmlns="http://www.w3.org/2000/svg" width="${P}" height="${P}" viewBox="0 0 ${P} ${P}"><rect width="${P}" height="${P}" fill="#fff"/>${el}</svg>`)
}
// Все простые пути start→end (каждый узел не более одного раза): [{cost, path:[индексы]}].
function allSimplePaths(adj, start, end) {
  const paths = []
  const rec = (u, visited, cost, path) => {
    if (u === end) { paths.push({ cost, path: path.slice() }); return }
    for (const [v, w] of adj[u]) {
      if (visited.has(v)) continue
      visited.add(v); path.push(v); rec(v, visited, cost + w, path); path.pop(); visited.delete(v)
    }
  }
  rec(start, new Set([start]), 0, [start])
  return paths
}
function t4ShortestPath() {
  const NAMES = ["A", "B", "C", "D", "E", "F"]
  for (let tries = 0; tries < 500; tries++) {
    const N = pick([5, 5, 5, 6])
    const nodes = NAMES.slice(0, N)
    const W = Array.from({ length: N }, () => Array(N).fill(0))
    for (let i = 0; i < N; i++) for (let j = i + 1; j < N; j++) if (Math.random() < 0.55) { const w = randInt(1, 9); W[i][j] = w; W[j][i] = w }
    const adj = nodes.map((_, i) => { const l = []; for (let j = 0; j < N; j++) if (W[i][j] > 0) l.push([j, W[i][j]]); return l })
    const start = 0, end = randInt(1, N - 1)
    const viaMode = Math.random() < 0.35
    const via = viaMode ? pick([...Array(N).keys()].filter((k) => k !== start && k !== end)) : -1
    let valid = allSimplePaths(adj, start, end)
    if (viaMode) valid = valid.filter((p) => p.path.includes(via))
    if (!valid.length) continue
    const best = Math.min(...valid.map((p) => p.cost))
    const bestPath = valid.find((p) => p.cost === best)
    if (bestPath.path.length < 3) continue                          // ≥2 рёбер — нетривиально
    if (!viaMode && W[start][end] > 0 && W[start][end] === best) continue   // прямое ребро не должно быть ответом
    return {
      condition_text: `Между населёнными пунктами ${nodes.join(", ")} построены дороги, протяжённость которых (в километрах) приведена в таблице.`,
      image_url: matrixTableSvg(nodes, W),
      condition_tail:
        `Определите длину кратчайшего пути между пунктами ${nodes[start]} и ${nodes[end]}` +
        `${viaMode ? `, проходящего через пункт ${nodes[via]}` : ""}. Передвигаться можно только по дорогам, ` +
        `протяжённость которых указана в таблице. Каждый пункт можно посетить только один раз.`,
      answer: String(best),
    }
  }
  return t4ShortestPath()
}

