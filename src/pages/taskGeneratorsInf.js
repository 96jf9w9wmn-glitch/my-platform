// Одно поле (без обёртки <svg>): сетка, стены (оранжевые), закраска target (если paint), Робот.
// Цвета — как в ФИПИ/решуОГЭ: оранжевые стены и мягкая оранжевая заливка закрашенных клеток.
function t15FieldBody(inst, paint) {
  const cs = 30, W = inst.C * cs, H = inst.R * cs
  const wall = (x1, y1, x2, y2) => `<line x1="${x1}" y1="${y1}" x2="${x2}" y2="${y2}" stroke="#d97a29" stroke-width="4" stroke-linecap="square"/>`
  let s = ""
  for (let r = 0; r < inst.R; r++) for (let c = 0; c < inst.C; c++) {
    const fill = paint && inst.target && inst.target.has(`${r},${c}`) ? "#f6cfa0" : "#fff"
    s += `<rect x="${c * cs}" y="${r * cs}" width="${cs}" height="${cs}" fill="${fill}" stroke="#d1d5db" stroke-width="1"/>`
  }
  inst.VW.forEach((k) => { const [r, c] = k.split(",").map(Number); s += wall((c + 1) * cs, r * cs, (c + 1) * cs, (r + 1) * cs) })
  inst.HW.forEach((k) => { const [r, c] = k.split(",").map(Number); s += wall(c * cs, (r + 1) * cs, (c + 1) * cs, (r + 1) * cs) })
  if (!inst.infinite) s += wall(0, 0, W, 0) + wall(0, H, W, H) + wall(0, 0, 0, H) + wall(W, 0, W, H)
  const cx = inst.start.c * cs + cs / 2, cy = inst.start.r * cs + cs / 2
  s += `<circle cx="${cx}" cy="${cy}" r="${cs * 0.3}" fill="#2563eb"/><text x="${cx}" y="${cy + 4}" text-anchor="middle" font-family="Arial" font-size="12" font-weight="bold" fill="#fff">Р</text>`
  return { body: s, W, H }
}

// Два поля рядом: слева — пример с закрашенными клетками, справа — заданное поле с Роботом.
function t15Image(exInst, taskInst) {
  const gap = 34
  const L = t15FieldBody(exInst, true), R = t15FieldBody(taskInst, false)
  const W = L.W + gap + R.W, H = Math.max(L.H, R.H)
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="-4 -4 ${W + 8} ${H + 8}" width="${W + 8}" height="${H + 8}">` +
    `<g transform="translate(0,0)">${L.body}</g><g transform="translate(${L.W + gap},0)">${R.body}</g></svg>`
}

