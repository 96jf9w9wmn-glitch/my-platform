// Стрелка от (x1,y1) к (x2,y2): линия отступает от обоих узлов, треугольник у цели.
function gArrow(x1, y1, x2, y2) {
  const dx = x2 - x1, dy = y2 - y1, len = Math.hypot(dx, dy) || 1
  const ux = dx / len, uy = dy / len, px = -uy, py = ux
  const sx = x1 + ux * 7, sy = y1 + uy * 7        // старт — от края узла
  const tx = x2 - ux * 8, ty = y2 - uy * 8        // остриё — перед узлом
  const bx = tx - ux * 9, by = ty - uy * 9
  return `<line x1="${sx.toFixed(1)}" y1="${sy.toFixed(1)}" x2="${tx.toFixed(1)}" y2="${ty.toFixed(1)}" stroke="#111" stroke-width="1.4"/>` +
    `<polygon points="${tx.toFixed(1)},${ty.toFixed(1)} ${(bx + px * 3.5).toFixed(1)},${(by + py * 3.5).toFixed(1)} ${(bx - px * 3.5).toFixed(1)},${(by - py * 3.5).toFixed(1)}" fill="#111"/>`
}
