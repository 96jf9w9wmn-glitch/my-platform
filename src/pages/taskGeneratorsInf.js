// ── №03 «Логические высказывания» (КЭС 2.8), 85 задач банка → 4 типажа ─────────
// Логическое выражение из атомов-предикатов (НЕ/И/ИЛИ) + вопрос-обёртка (наим./наиб.
// число, количество, выбор из четырёх). Ответ всегда получаем брутфорсом по области —
// это исключает ошибку при любой формуле. Знаки < > печатаем с пробелами (безопасно в HTML).
const A_gt = (v, k) => ({ text: `(${v} > ${k})`, f: (x) => x > k })
const A_lt = (v, k) => ({ text: `(${v} < ${k})`, f: (x) => x < k })
const A_even = (v) => ({ text: `(${v} чётное)`, f: (x) => x % 2 === 0 })
const A_odd = (v) => ({ text: `(${v} нечётное)`, f: (x) => x % 2 !== 0 })
const A_mult = (v, k) => ({ text: `(${v} кратно ${k})`, f: (x) => x % k === 0 })
const A_ends = (v, d) => ({ text: `(${v} оканчивается на ${d})`, f: (x) => x % 10 === d })

const bAtom = (a) => ({ kind: "atom", text: a.text, f: a.f })
const bNot = (n) => ({ kind: "not", child: n })
function evalN(n, x) {
  return n.kind === "atom" ? n.f(x)
    : n.kind === "not" ? !evalN(n.child, x)
      : n.kind === "and" ? (evalN(n.a, x) && evalN(n.b, x))
        : (evalN(n.a, x) || evalN(n.b, x))
}
function renderN(n, top) {
  if (n.kind === "atom") return n.text
  if (n.kind === "not") return `НЕ ${renderN(n.child, false)}`
  const s = `${renderN(n.a, false)} ${n.kind === "and" ? "И" : "ИЛИ"} ${renderN(n.b, false)}`
  return top ? s : `(${s})`
}
const maybeNot = (n) => (Math.random() < 0.5 ? bNot(n) : n)
const connKind = () => pick(["and", "or"])
function buildExpr(atoms) {
  const ns = atoms.map(bAtom)
  if (ns.length === 2) return { kind: connKind(), a: maybeNot(ns[0]), b: maybeNot(ns[1]) }
  const pair = { kind: connKind(), a: ns[0], b: maybeNot(ns[1]) }
  const left = Math.random() < 0.35 ? bNot(pair) : pair
  return { kind: connKind(), a: left, b: maybeNot(ns[2]) }
}
function distinctAtoms(pool, n) {
  const out = []
  for (let g = 0; g < 60 && out.length < n; g++) {
    const a = pick(pool)()
    if (!out.some((o) => o.text === a.text)) out.push(a)
  }
  while (out.length < n) out.push(A_even(out[0] ? "x" : "x"))
  return out
}
const atomsNatural = (n, v) => distinctAtoms([
  () => A_gt(v, randInt(2, 12)), () => A_lt(v, randInt(3, 13)),
  () => A_even(v), () => A_odd(v), () => A_mult(v, randInt(2, 6)),
], n)
const atomsRanged = (n, v, lo, hi) => distinctAtoms([
  () => A_gt(v, randInt(lo + 2, hi - 2)), () => A_lt(v, randInt(lo + 2, hi - 2)),
  () => A_even(v), () => A_odd(v), () => A_mult(v, randInt(3, 19)), () => A_ends(v, randInt(0, 9)),
], n)
function satisfy(node, lo, hi, polarity) {
  const out = []
  for (let x = lo; x <= hi; x++) if (evalN(node, x) === polarity) out.push(x)
  return out
}
const truthWord = (pol) => (pol ? "истинно" : "ложно")

