const spaced2 = (s) => [...s].join(" ")   // «+~+@» → «+ ~ + @»

// Все расшифровки цепочки по таблице (до limit различных).
function decodings2(codeMap, chain, limit) {
  const entries = Object.entries(codeMap)
  const res = []
  const rec = (pos, acc) => {
    if (res.length >= limit) return
    if (pos === chain.length) { res.push(acc.join("")); return }
    for (const [ltr, code] of entries) if (chain.startsWith(code, pos)) rec(pos + code.length, acc.concat(ltr))
  }
  rec(0, [])
  return res
}
// Расшифровки, в которых ни одна буква не повторяется (до limit различных).
function decodingsNoRepeat(codeMap, chain, limit) {
  const entries = Object.entries(codeMap)
  const res = []
  const rec = (pos, acc, used) => {
    if (res.length >= limit) return
    if (pos === chain.length) { res.push(acc.join("")); return }
    for (const [ltr, code] of entries) {
      if (used.has(ltr) || !chain.startsWith(code, pos)) continue
      used.add(ltr); rec(pos + code.length, acc.concat(ltr), used); used.delete(ltr)
    }
  }
  rec(0, [], new Set())
  return res
}
