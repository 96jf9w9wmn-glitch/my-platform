// ── Разметка образца: **жирный**, *курсив*, __подчёркнутый__ → массив ранов {text,b,i,u} ──
function parseRuns(str) {
  const runs = []
  const flags = { b: false, i: false, u: false }
  let buf = ""
  const flush = () => { if (buf) { runs.push({ text: buf, ...flags }); buf = "" } }
  for (let k = 0; k < str.length;) {
    if (str.startsWith("**", k)) { flush(); flags.b = !flags.b; k += 2 }
    else if (str.startsWith("__", k)) { flush(); flags.u = !flags.u; k += 2 }
    else if (str[k] === "*") { flush(); flags.i = !flags.i; k += 1 }
    else { buf += str[k]; k += 1 }
  }
  flush()
  return runs
}
// Разбить раны на слова (границы — пробелы); слово может состоять из ранов разного стиля.
function wordsFromRuns(runs) {
  const words = []
  let cur = null
  const push = (t, r) => { if (t) { (cur ||= { frags: [] }).frags.push({ text: t, b: r.b, i: r.i, u: r.u }) } }
  const end = () => { if (cur) { words.push(cur); cur = null } }
  runs.forEach((r) => r.text.split(" ").forEach((p, i) => { if (i > 0) end(); push(p, r) }))
  end()
  return words
}
// Приблизительная ширина символа при размере 13px (Arial). Нужна ТОЛЬКО для расстановки
// переносов и растяжки пробелов при выравнивании по ширине; сами слова рисуются <tspan>'ами,
// поэтому ширина внутри слова браузером считается точно (зазоров/наездов у пунктуации нет).
const T13_FS = 13, T13_SPACE = 3.6
const charW = (ch) => {
  if (ch === " ") return T13_SPACE
  if (ch === "—" || ch === "–") return 11
  if (/[.,:;!?()'’·-]/.test(ch)) return 3.9
  if (/[a-zа-яё]/.test(ch)) return 7.2
  if (/[A-ZА-ЯЁ0-9«»%]/.test(ch)) return 8.4
  return 7.2
}
const fragW = (f) => [...f.text].reduce((s, ch) => s + charW(ch) * (f.b ? 1.05 : 1), 0)
const wordW = (w) => w.frags.reduce((s, f) => s + fragW(f), 0)
// Слово → tspans (стиль на каждый фрагмент); фрагменты внутри слова выкладываются встык.
const wordTspans = (frags) => frags.map((f) => {
  const st = (f.b ? ` font-weight="bold"` : "") + (f.i ? ` font-style="italic"` : "") + (f.u ? ` text-decoration="underline"` : "")
  return st ? `<tspan${st}>${escXml(f.text)}</tspan>` : escXml(f.text)
}).join("")
const T13_TEXT = (x, y, inner, extra = "") => `<text x="${x}" y="${y}" font-family="Arial,sans-serif" font-size="${T13_FS}" fill="#111"${extra}>${inner}</text>`

// Абзац по ширине. Строку рисуем ОДНИМ <text> и растягиваем нативным textLength/lengthAdjust —
// браузер сам выравнивает по ширине точно, оценка нужна лишь для разбиения на строки.
function paragraphSvg(str, x0, width, yStart, indent, lineH) {
  const words = wordsFromRuns(parseRuns(str))
  const lines = []
  let line = [], lw = 0, firstLine = true
  words.forEach((w) => {
    const ww = wordW(w)
    const avail = width - (firstLine ? indent : 0)
    if (line.length && lw + T13_SPACE + ww > avail) { lines.push({ words: line, first: firstLine }); firstLine = false; line = []; lw = 0 }
    lw += (line.length ? T13_SPACE : 0) + ww
    line.push(w)
  })
  if (line.length) lines.push({ words: line, first: firstLine, last: true })
  let svg = "", y = yStart
  lines.forEach((L) => {
    const startX = x0 + (L.first ? indent : 0)
    const avail = width - (L.first ? indent : 0)
    const inner = L.words.map((w) => wordTspans(w.frags)).join(" ")
    const tl = (!L.last && L.words.length > 1) ? ` textLength="${avail.toFixed(0)}" lengthAdjust="spacing"` : ""
    svg += T13_TEXT(startX, y, inner, tl)
    y += lineH
  })
  return { svg, y }
}
// Ячейка таблицы: весь текст одним <text>; центрирование — через text-anchor (без оценки ширины).
function cellSvg(str, cx, cy, cw, align) {
  const inner = wordTspans(parseRuns(str).map((r) => ({ text: r.text, b: r.b, i: r.i, u: r.u })))
  return align === "center"
    ? T13_TEXT(cx + cw / 2, cy, inner, ` text-anchor="middle"`)
    : T13_TEXT(cx + 8, cy, inner)
}
// Тело образца текстового документа при origin (ox, oy): «страница» (белый прямоугольник),
// заголовок по центру (полужирный, прописные), таблица в 2 столбца уже ширины текста и по
// центру, затем абзацы по ширине с отступом первой строки. Возвращает {svg, w, h}.
function textDocBody(doc, ox, oy) {
  const M = 30, CW = 600, W = CW + 2 * M          // поля и ширина «страницы»
  const rect = (x, y, w, h, fill) => `<rect x="${x}" y="${y}" width="${w}" height="${h}" fill="${fill}" stroke="#c8ccd4"/>`
  let body = "", y = oy + 42
  body += `<text x="${ox + M + CW / 2}" y="${y}" text-anchor="middle" font-family="Arial,sans-serif" font-size="15" font-weight="bold" fill="#111">${escXml(doc.title)}</text>`
  y += 30
  // Таблица (уже текста, по центру): шапка по центру, 1-й столбец по левому краю, 2-й — по центру.
  const tW = Math.round(CW * 0.62), tX = ox + M + (CW - tW) / 2, col1 = Math.round(tW * 0.62), col2 = tW - col1, rowH = 28
  const rows = [doc.tableHead, ...doc.tableRows]
  rows.forEach((r, ri) => {
    const ry = y + ri * rowH, header = ri === 0
    body += rect(tX, ry, col1, rowH, header ? "#eef0f4" : "#fff")
    body += rect(tX + col1, ry, col2, rowH, header ? "#eef0f4" : "#fff")
    const ty = ry + rowH / 2 + 5
    body += cellSvg(header ? `**${r[0]}**` : r[0], tX, ty, col1, header ? "center" : "left")
    body += cellSvg(header ? `**${r[1]}**` : r[1], tX + col1, ty, col2, "center")
  })
  y += rows.length * rowH + 24
  // Абзацы основного текста (по ширине, отступ первой строки ≈ 1 см = 38 px при CW=600)
  doc.paragraphs.forEach((p) => {
    const r = paragraphSvg(p, ox + M, CW, y, 38, 20)
    body += r.svg; y = r.y + 8
  })
  const H = Math.ceil(y + 18 - oy)
  return { svg: rect(ox, oy, W, H, "#fff") + body, w: W, h: H }
}

