// Датчики поля: стены — множества «r,c». VW: стена справа от (r,c). HW: стена снизу от (r,c).
const t15Sensors = (inst) => ({
  fR: (r, c) => c < inst.C - 1 && !inst.VW.has(`${r},${c}`),
  fL: (r, c) => c > 0 && !inst.VW.has(`${r},${c - 1}`),
  fD: (r, c) => r < inst.R - 1 && !inst.HW.has(`${r},${c}`),
  fU: (r, c) => r > 0 && !inst.HW.has(`${r - 1},${c}`),
})
// Прогон JS-зеркала алгоритма на поле — для проверки (в самом генераторе не используется).
function t15Run(inst, mirror) {
  const s = t15Sensors(inst)
  const bot = { r: inst.start.r, c: inst.start.c, painted: new Set(), crashed: false, steps: 0 }
  const guard = () => { if (++bot.steps > 200000) throw new Error("loop") }
  const mv = (cond, dr, dc) => { guard(); if (cond()) { bot.r += dr; bot.c += dc } else bot.crashed = true }
  const api = {
    freeUp: () => s.fU(bot.r, bot.c), freeDown: () => s.fD(bot.r, bot.c),
    freeLeft: () => s.fL(bot.r, bot.c), freeRight: () => s.fR(bot.r, bot.c),
    up: () => mv(api.freeUp, -1, 0), down: () => mv(api.freeDown, 1, 0),
    left: () => mv(api.freeLeft, 0, -1), right: () => mv(api.freeRight, 0, 1),
    paint: () => bot.painted.add(`${bot.r},${bot.c}`),
  }
  try { mirror(api) } catch { bot.crashed = true }
  return bot
}

