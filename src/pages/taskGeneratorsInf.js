const T12_BASE = ["Глава", "Страница", "Заметка", "Список", "Рисунок", "Эскиз", "Полотно",
  "Стих", "Отрывок", "Скан", "Фрагмент", "Документ", "Черновик", "Копия"]
// Перечисление через запятую с «и» перед последним: [A,B,C] → «A, B и C».
const enumRu = (list) => list.length <= 1 ? (list[0] || "")
  : `${list.slice(0, -1).join(", ")} и ${list[list.length - 1]}`
const subWord = (n) => n === 1 ? "подкаталоге" : "подкаталогах"

function t12FileCount() {
  // 1) Строим случайное дерево: по каждому жанру берём подмножество авторов, у каждого — файлы.
  const files = {}
  const used = {}                         // жанр → массив выбранных авторов
  for (const [genre, cfg] of Object.entries(T12_GENRES)) {
    const authors = shuffle(cfg.authors).slice(0, randInt(4, 6))
    used[genre] = authors
    authors.forEach((a) => {
      const seen = new Set()
      const cnt = randInt(3, 9)
      for (let i = 0; i < cnt; i++) {
        const ext = pick(cfg.exts)
        let name = `${pick(T12_BASE)}.${ext}`
        while (seen.has(name)) name = `${pick(T12_BASE)}_${i}.${ext}`
        seen.add(name)
        files[`${T12_ROOT}/${genre}/${a}/${name}`] = ""   // содержимое не важно — считаем по имени
      }
    })
  }
  // Счётчик: файлы с расширением из exts, лежащие в любой из папок-prefix.
  const count = (prefixes, exts) => Object.keys(files).filter((p) =>
    prefixes.some((pre) => p.startsWith(pre + "/")) && exts.some((e) => p.endsWith("." + e))).length
  const extList = (es) => es.length === 1 ? `с расширением .${es[0]}` : `с расширениями ${es.map((e) => "." + e).join(" и ")}`

  const genres = Object.keys(used)
  const kind = pick(["all-root", "all-genre", "some-genre", "two-genre", "two-genre"])
  let question, answer
  if (kind === "all-root") {
    const e = pick(pick(Object.values(T12_GENRES)).exts)
    answer = count([T12_ROOT], [e])
    question = `Сколько файлов с расширением .${e} содержится в подкаталогах каталога ${T12_ROOT}?`
  } else if (kind === "all-genre") {
    const g = pick(genres)
    const e = pick(T12_GENRES[g].exts)
    answer = count([`${T12_ROOT}/${g}`], [e])
    question = `Сколько файлов с расширением .${e} содержится в подкаталогах каталога ${g}?`
  } else if (kind === "some-genre") {
    const g = pick(genres)
    const auth = shuffle(used[g]).slice(0, randInt(2, 3))
    const e = pick(T12_GENRES[g].exts)
    answer = count(auth.map((a) => `${T12_ROOT}/${g}/${a}`), [e])
    question = `Сколько файлов c расширением .${e} содержится в ${subWord(auth.length)} ${enumRu(auth)} каталога ${T12_ROOT}/${g}?`
  } else {
    // два жанра, два расширения, по несколько подкаталогов в каждом
    const [g1, g2] = shuffle(genres).slice(0, 2)
    const a1 = shuffle(used[g1]).slice(0, randInt(1, 2))
    const a2 = shuffle(used[g2]).slice(0, randInt(1, 2))
    const extPool = [...new Set([...T12_GENRES[g1].exts, ...T12_GENRES[g2].exts])]
    const es = shuffle(extPool).slice(0, 2)
    const prefixes = [...a1.map((a) => `${T12_ROOT}/${g1}/${a}`), ...a2.map((a) => `${T12_ROOT}/${g2}/${a}`)]
    answer = count(prefixes, es)
    question = `Сколько всего файлов ${extList(es)} содержится в ${subWord(a1.length)} ${enumRu(a1)} каталога ${T12_ROOT}/${g1}, а также в ${subWord(a2.length)} ${enumRu(a2)} каталога ${T12_ROOT}/${g2}?`
  }
  return {
    condition_text:
      `Задание выполняется с использованием прилагаемых файлов.\n\n` +
      `${question} В ответе укажите только число.`,
    answer: String(answer),
    archive: { name: `${T12_ROOT}.zip`, files },
  }
}

// ── №14 «Обработка данных в электронной таблице» (КЭС 3.5), с файлом .xlsx ──────
// Механика ФИПИ: таблица на 1000 строк; два вопроса — обычно (1) счёт строк по условию и
// (2) среднее по столбцу среди подмножества, округлённое до 2 знаков. Мы генерируем данные,
// СЧИТАЕМ оба ответа точно по массиву, показываем образец первых 5 строк (SVG-таблица как в
// ФИПИ) и прикладываем настоящий .xlsx (xlsxWriter). Ответ — два числа.
const T14_N = 1000
const ruNum = (v) => typeof v === "number" ? String(v).replace(".", ",") : String(v)
const round2 = (x) => Math.round(x * 100) / 100
const avgOf = (rows, col) => rows.length ? round2(rows.reduce((s, r) => s + r[col], 0) / rows.length) : 0
const LET = ["A", "B", "C", "D", "E", "F", "G"]

// Образец таблицы: строка-буквы (A B C…), строка-заголовки (row 1) и 5 строк данных (2–6),
// с левым столбцом номеров строк — визуально как электронная таблица ФИПИ.
function sheetSampleSvg(headers, dataRows) {
  const cols = headers.length
  const disp = [headers, ...dataRows.slice(0, 5)]                 // 6 строк (1 шапка + 5 данных)
  const cellText = (v) => escXml(ruNum(v))
  const padX = 11, rowH = 24, numW = 34
  // Оценка ширины: шапка жирная (шире), поэтому для неё коэффициент больше. Берём максимум
  // по всем строкам столбца, чтобы длинный заголовок/значение помещались и не обрезались.
  const colW = headers.map((_, c) => {
    const w = Math.max(...disp.map((r, ri) => cellText(r[c]).length * (ri === 0 ? 9.6 : 8.4)))
    return Math.max(48, Math.min(320, Math.ceil(w) + padX * 2))
  })
  const xs = [0, numW]
  colW.forEach((w) => xs.push(xs[xs.length - 1] + w))
  const W = xs[xs.length - 1], H = rowH * (disp.length + 1)
  const rect = (x, y, w, h, fill) => `<rect x="${x}" y="${y}" width="${w}" height="${h}" fill="${fill}" stroke="#c8ccd4"/>`
  const txt = (x, y, s, opts = "") => `<text x="${x}" y="${y}" font-family="Arial,sans-serif" font-size="13" ${opts}>${s}</text>`
  let svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${W} ${H}" width="${W}" height="${H}">`
  // строка буквенных меток колонок
  svg += rect(0, 0, numW, rowH, "#eef0f4")
  for (let c = 0; c < cols; c++) {
    svg += rect(xs[c + 1], 0, colW[c], rowH, "#eef0f4")
    svg += txt(xs[c + 1] + colW[c] / 2, rowH - 8, LET[c], `text-anchor="middle" font-weight="bold" fill="#555"`)
  }
  // строки данных (row 1 — шапка, 2..6 — данные)
  disp.forEach((r, ri) => {
    const y = rowH * (ri + 1)
    svg += rect(0, y, numW, rowH, "#eef0f4")
    svg += txt(numW / 2, y + rowH - 8, String(ri + 1), `text-anchor="middle" font-weight="bold" fill="#555"`)
    for (let c = 0; c < cols; c++) {
      svg += rect(xs[c + 1], y, colW[c], rowH, ri === 0 ? "#f6f7f9" : "#fff")
      const s = cellText(r[c])
      svg += txt(xs[c + 1] + padX, y + rowH - 8, s, ri === 0 ? `font-weight="bold"` : "")
    }
  })
  return svg + "</svg>"
}

