// Слова с уникальной длиной внутри пула (кандидаты в ответ — иначе неоднозначно).
const uniqueLenWords = (words) => {
  const cnt = {}
  words.forEach((w) => { cnt[wlen(w)] = (cnt[wlen(w)] || 0) + 1 })
  return words.filter((w) => cnt[wlen(w)] === 1)
}
// Кандидаты-слова в связном тексте: «чистое» слово (буквы/дефис, без знаков препинания),
// длина которого уникальна среди длин ВСЕХ слов (знаки препинания отброшены). Это делает
// однозначным ответ: только одно слово в тексте даёт данную величину Δ.
const strip = (w) => w.replace(/[^А-Яа-яЁё-]/g, "")
const textCandidates = (t) => {
  const toks = t.split(/\s+/)
  const cnt = {}
  toks.forEach((w) => { const n = wlen(strip(w)); cnt[n] = (cnt[n] || 0) + 1 })
  return toks.filter((w) => /^[А-Яа-яЁё-]+$/.test(w) && cnt[wlen(w)] === 1)
}

