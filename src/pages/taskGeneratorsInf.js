// ── №05 «Алгоритм-исполнитель» (КЭС 3.4), 103 задачи банка → 1 типаж ───────────
// Исполнитель с двумя командами (одна аддитивная: вычти/прибавь; одна мультипликативная:
// умножь на / возведи в квадрат). Надо получить из числа A число B не более чем за 5 команд;
// ответ — последовательность номеров команд. Путь ищем BFS — это гарантирует и решаемость,
// и корректность ответа (кратчайшая цепочка длиной 3–5).
const cSub = (k) => ({ label: `вычти ${k}`, desc: `уменьшает число на экране на ${k}`, op: (x) => x - k })
const cAdd = (k) => ({ label: `прибавь ${k}`, desc: `прибавляет к числу ${k}`, op: (x) => x + k })
const cMul = (k) => ({ label: `умножь на ${k}`, op: (x) => x * k,
  desc: k === 2 ? "удваивает число на экране" : k === 3 ? "утраивает число на экране" : `увеличивает число на экране в ${k} ${k >= 5 ? "раз" : "раза"}` })
const cSq = () => ({ label: "возведи в квадрат", desc: "возводит число на экране во вторую степень", op: (x) => x * x })

// BFS от A по двум командам: value → кратчайшая последовательность номеров ("1"/"2"), глубина ≤ maxLen.
function bfsExec(A, cmds, maxLen, lo, hi) {
  const dist = new Map([[A, ""]])
  let frontier = [A]
  for (let step = 0; step < maxLen; step++) {
    const next = []
    for (const v of frontier) {
      const seq = dist.get(v)
      for (let ci = 0; ci < 2; ci++) {
        const nv = cmds[ci].op(v)
        if (nv < lo || nv > hi || dist.has(nv)) continue
        dist.set(nv, seq + (ci + 1)); next.push(nv)
      }
    }
    frontier = next
  }
  return dist
}
function execName(mulCmd) {
  const neutral = ["Вычислитель", "Калькулятор", "Программист", "Умножатор"]
  const special = mulCmd.label === "возведи в квадрат" ? "Квадратор"
    : mulCmd.label === "умножь на 2" ? "Удвоитель"
      : mulCmd.label === "умножь на 3" ? "Утроитель" : null
  return special && Math.random() < 0.6 ? special : pick(neutral)
}

function t5Executor() {
  for (let tries = 0; tries < 400; tries++) {
    const addCmd = pick([cSub(randInt(1, 5)), cAdd(randInt(1, 4))])
    const mulCmd = pick([cMul(randInt(2, 5)), cSq()])
    const cmds = shuffle([addCmd, mulCmd])                    // порядок = номера 1 и 2
    const A = randInt(1, 8)
    const dist = bfsExec(A, cmds, 5, 1, 100000)
    const cands = [...dist.entries()].filter(([v, s]) => v !== A && s.length >= 3 && s.length <= 5 && v >= 5 && v <= 400)
    if (!cands.length) continue
    const [B, seq] = pick(cands)
    // пример-иллюстрация записи: случайная цепочка из 3 команд от A, не выходящая за границы
    let ex = "", cur = A
    for (let i = 0; i < 3; i++) { const ci = randInt(0, 1); const nv = cmds[ci].op(cur); if (nv < 1 || nv > 100000) { ex = ""; break } ex += String(ci + 1); cur = nv }
    if (!ex) continue
    const exExpanded = ex.split("").map((d) => cmds[+d - 1].label).join(", ")
    return {
      condition_text:
        `У исполнителя ${execName(mulCmd)} две команды, которым присвоены номера: ` +
        `1. ${cmds[0].label} 2. ${cmds[1].label} Первая из них ${cmds[0].desc}, вторая ${cmds[1].desc}. ` +
        `Составьте алгоритм получения из числа ${A} числа ${B}, содержащий не более пяти команд. ` +
        `В ответе запишите только номера команд. ` +
        `(Например, ${ex} — это алгоритм: ${exExpanded}, который преобразует число ${A} в число ${cur}.)`,
      answer: seq,
    }
  }
  return t5Executor()
}

