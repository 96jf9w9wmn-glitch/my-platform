const randInt = (a, b) => a + Math.floor(Math.random() * (b - a + 1))
const pick = (a) => a[Math.floor(Math.random() * a.length)]
const shuffle = (a) => { a = a.slice(); for (let i = a.length - 1; i > 0; i--) { const j = randInt(0, i);[a[i], a[j]] = [a[j], a[i]] } return a }
const digitsAsc = (arr) => arr.slice().sort((x, y) => x - y).join("")

// ── Движок «Верны ли суждения А и Б» ──────────────────────────────────────────
// pool: { <тема>: [{ t: "формулировка", v: true|false }, …] }. Оба суждения — об одной теме
// (как в ФИПИ). Ответ: ТT→3, ТЛ→1, ЛТ→2, ЛЛ→4.
function makeAB(pool) {
  const topics = Object.keys(pool)
  return () => {
    const topic = pick(topics)
    const [a, b] = shuffle(pool[topic]).slice(0, 2)
    const code = a.v && b.v ? 3 : a.v && !b.v ? 1 : !a.v && b.v ? 2 : 4
    const ct =
      `Выберите правильный ответ.\n` +
      `Верны ли следующие суждения о ${topic}?\n` +
      `А. ${a.t}\n` +
      `Б. ${b.t}\n` +
      `1) верно только А\n` +
      `2) верно только Б\n` +
      `3) верны оба суждения\n` +
      `4) оба суждения неверны`
    return { condition_text: ct, answer: String(code) }
  }
}

// ── Движок «Одиночный выбор из 4» ─────────────────────────────────────────────
// pool: [{ q: "вопрос", correct: "верный вариант", wrong: ["…","…","…"] }].
function makeChoice(pool) {
  return () => {
    const it = pick(pool)
    const wrong = shuffle(it.wrong).slice(0, 3)
    const opts = shuffle([{ t: it.correct, ok: true }, ...wrong.map((t) => ({ t, ok: false }))])
    const ans = opts.findIndex((o) => o.ok) + 1
    const lines = opts.map((o, i) => `${i + 1}) ${o.t}`).join("\n")
    return { condition_text: `Выберите правильный ответ.\n${it.q}\n${lines}`, answer: String(ans) }
  }
}

