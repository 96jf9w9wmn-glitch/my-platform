// n случайных долей, в сумме 100, каждая ≥ minv
function partition100(n, minv = 6) {
  let rest = 100 - minv * n, parts = Array(n).fill(minv)
  const w = Array.from({ length: n }, () => Math.random())
  const sum = w.reduce((a, b) => a + b, 0)
  const add = w.map((x) => Math.round((x / sum) * rest))
  let diff = rest - add.reduce((a, b) => a + b, 0)
  add[0] += diff
  return parts.map((p, i) => p + add[i])
}

