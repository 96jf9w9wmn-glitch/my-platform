// ── №3, №5: распознавание/объяснение явлений (одиночный выбор) ────────────────
// Банк курируемых пунктов {q, opts:[4 варианта], c: индекс правильного}. Варианты
// перемешиваем → ключ считается по позиции правильного (не «прибит» к одному номеру).
const CHOICE_LEAD = "Выберите правильный ответ.\n\n"
function choiceGen(bank) {
  const it = pick(bank)
  const order = shuffle(it.opts.map((t, i) => ({ t, correct: i === it.c })))
  const numbered = order.map((o, i) => `${i + 1}) ${o.t}`).join("\n")
  return {
    condition_text: `${CHOICE_LEAD}${it.q}\n\n${numbered}`,
    answer: String(order.findIndex((o) => o.correct) + 1),
  }
}

