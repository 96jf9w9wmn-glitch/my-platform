import { matchBlock } from "../utils"

const randInt = (min, max) => min + Math.floor(Math.random() * (max - min + 1))
const pick = (arr) => arr[Math.floor(Math.random() * arr.length)]
const shuffle = (arr) => { const a = arr.slice(); for (let i = a.length - 1; i > 0; i--) { const j = randInt(0, i);[a[i], a[j]] = [a[j], a[i]] } return a }

// Число в стиле ФИПИ: целое — как есть, дробное — с запятой (без хвостовых нулей).
function nf(x) {
  const r = Math.round(x * 1000) / 1000
  return Number.isInteger(r) ? String(r) : String(r).replace(".", ",")
}

const cap = (s) => s.charAt(0).toUpperCase() + s.slice(1)

// ── Универсальный движок «соответствия» (№1, №2) ─────────────────────────────
// topic = { intro, leftHdr, rightHdr, pairs: [[left, right], …], distractors: [right…],
//           sizes: [2,3], optCount: 4 }.
// Берём k пар с РАЗНЫМИ right, собираем варианты (правые ответы + дистракторы того же
// набора, дополняя до optCount), перемешиваем, ключ считаем по позиции right в вариантах.
function matchGen(topic) {
  const k = pick(topic.sizes)
  // Пары с уникальными right (в задании каждая буква ссылается на отдельную цифру).
  const seen = new Set()
  const uniq = shuffle(topic.pairs).filter(([, r]) => (seen.has(r) ? false : seen.add(r)))
  const chosen = uniq.slice(0, k)
  const correctR = chosen.map(([, r]) => r)
  // Дистракторы — правые варианты того же набора, не совпадающие с правильными.
  const distract = shuffle(topic.distractors.filter((d) => !correctR.includes(d)))
  const optCount = topic.optCount || Math.max(4, k + 1)
  const options = shuffle([...correctR, ...distract].slice(0, optCount))
  const answer = chosen.map(([, r]) => options.indexOf(r) + 1).join("")
  return {
    condition_text: `${topic.intro}\n\n` + matchBlock({
      leftHdr: topic.leftHdr, rightHdr: topic.rightHdr,
      left: chosen.map(([l]) => l), right: options,
    }),
    answer,
  }
}

