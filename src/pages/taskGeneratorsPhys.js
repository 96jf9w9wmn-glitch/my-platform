// Прямой вопрос: дан исходный изотоп, спрашивается A или Z продукта.
function t11DecayForward() {
  const alpha = Math.random() < 0.5
  const it = pick(alpha ? ALPHA_ISO : BETA_ISO)
  const askA = Math.random() < 0.5
  const kind = alpha ? "α-распад" : "β⁻-распад"
  const A2 = alpha ? it.A - 4 : it.A
  const Z2 = alpha ? it.Z - 2 : it.Z + 1
  const what = askA ? "массовое" : "зарядовое"
  return {
    condition_text: `Радиоактивный изотоп ${it.gen} ${iso(it.A, it.Z, it.sym)} испытывает ${kind}. ` +
      `Чему равно ${what} число ядра, полученного в результате этого распада?`,
    answer: String(askA ? A2 : Z2),
  }
}

