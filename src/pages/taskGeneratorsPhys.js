// ── №9: электростатика (расчёт) ──────────────────────────────────────────────

// Закон Кулона: F ∝ q₁·q₂/r². «Во сколько раз увеличится сила, если каждый заряд
// увеличить в a раз, а расстояние изменить в b раз?» Комбинации подобраны так, что
// множитель ≥ 1 (иначе «увеличится» некорректно).
const COULOMB_COMBOS = [
  { a: 4, b: 2, mode: "up" },   // a²/b² = 4
  { a: 3, b: 2, mode: "up" },   // 9/4 = 2,25
  { a: 2, b: 2, mode: "down" }, // a²·b² = 16
  { a: 2, b: 3, mode: "down" }, // 36
  { a: 3, b: 2, mode: "down" }, // 36
  { a: 4, b: 2, mode: "down" }, // 64
  { a: 3, b: 3, mode: "down" }, // 81
]
function t9Coulomb() {
  const c = pick(COULOMB_COMBOS)
  const factor = c.mode === "down" ? c.a * c.a * c.b * c.b : (c.a * c.a) / (c.b * c.b)
  const distPhrase = c.mode === "down" ? `уменьшить в ${c.b} раза` : `увеличить в ${c.b} раза`
  return {
    condition_text: `Во сколько раз увеличится сила взаимодействия двух неподвижных точечных зарядов, ` +
      `если величину каждого из зарядов увеличить в ${c.a} раза, а расстояние между ними ${distPhrase}?`,
    answer: nf(factor),
  }
}

// Соединение двух одинаковых шариков: заряд каждого становится (q₁+q₂)/2. Пары подобраны
