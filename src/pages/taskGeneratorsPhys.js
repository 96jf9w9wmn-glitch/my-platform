const G = 10   // ускорение свободного падения на ОГЭ, м/с²

// Средняя скорость: путь S за время t → v = S/t (переводим в м/с).
function t6AvgSpeed() {
  const vTarget = pick([0.5, 1, 1.5, 2, 2.5, 3, 4, 5])
  const tMin = pick([1, 2, 3, 4, 5, 10, 20])
  const S = Math.round(vTarget * tMin * 60)   // метры
  const subj = pick([
    `Пешеход, двигаясь по шоссе, прошёл ${S} м за ${tMin} мин.`,
    `Велосипедист за ${tMin} мин проехал по прямой дороге ${S} м.`,
    `Лыжник за ${tMin} мин прошёл по дистанции ${S} м.`,
  ])
  return {
    condition_text: `${subj} Чему равна его средняя скорость? Ответ выразите в ⟦f:м:с⟧.`,
    answer: nf(S / (tMin * 60)),
  }
}

