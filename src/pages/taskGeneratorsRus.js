function gen6() {
  const items = shuffle(P6).slice(0, 5)
  const correct = []
  const lines = items.map((it, i) => {
    const good = Math.random() < 0.5
    if (good) correct.push(i + 1)
    return `${i + 1}) ${it.word} – ${good ? it.ok : it.bad}`
  })
  // гарантируем 2–4 верных (не 0, не 5): при перекосе перевернём крайний вариант
  if (correct.length === 0) { correct.push(1); lines[0] = `1) ${items[0].word} – ${items[0].ok}` }
  if (correct.length === 5) { const idx = correct.pop(); lines[idx - 1] = `${idx}) ${items[idx - 1].word} – ${items[idx - 1].bad}` }
  const instr = "Укажите варианты ответов, в которых дано верное объяснение написания выделенного слова. Запишите номера ответов."
  return { condition_text: `${instr}\n\n${lines.join("\n")}`, answer: digits(correct) }
}

