-- Интерактивный тест с выбором ответа (multiple-choice) в домашних заданиях.
-- Когда репетитор генерирует ДЗ типа «Тест» через ИИ и включает варианты ответа,
-- к каждому вопросу прикладывается набор вариантов. Ученик выбирает один из них,
-- правильный ответ уже лежит в correct_answers[i] — тест проверяется автоматически.
--
-- test_options — массив по вопросам: [["4","5","6","7"], ["да","нет"], ...].
-- Индекс i соответствует i-му вопросу теста и correct_answers[i].
-- null / отсутствует — обычный тест со свободным вводом ответа (как раньше).
--
-- Выполнить один раз в Supabase → SQL Editor. Идемпотентно (IF NOT EXISTS).

ALTER TABLE homework ADD COLUMN IF NOT EXISTS test_options jsonb;

